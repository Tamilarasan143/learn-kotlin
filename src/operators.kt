fun main() {
    var a = 10;
    var b = 5;
    println(a + b)
    println(a - b)
    //pre increment and decrement
    println(++a);
    println(a);
    println(--b);
    println(b);
    //post increment and decrement
    println(a++);
    println(a);
    println(b--);
    println(b);
    //logic operators
    println(a < b);
    println(a > b);
    // ===
    println(a === b)
    //==
    println(a == b);
    println(a != b);
}